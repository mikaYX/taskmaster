#!/usr/bin/env node
/**
 * Validates the security exceptions allowlist (security/trivy-exceptions.json).
 *
 * For each exception:
 *   - Checks required fields (id, reason, owner, expiresAt)
 *   - Flags expired entries (expiresAt < today)
 *   - Flags entries expiring within 30 days
 *
 * Outputs:
 *   - .trivyignore           (list of still-valid CVE IDs for Trivy)
 *   - security-exceptions-report.json  (detailed validation report for CI summary)
 *
 * Exits 1 if any expired exception is found (expired entries must be removed).
 */

'use strict';

const fs = require('fs');
const path = require('path');

const EXCEPTIONS_FILE = path.join(__dirname, '..', 'security', 'trivy-exceptions.json');
const TRIVYIGNORE_FILE = path.join(__dirname, '..', '.trivyignore');
const REPORT_FILE = path.join(__dirname, '..', 'security-exceptions-report.json');
const WARNING_DAYS = 30;

if (!fs.existsSync(EXCEPTIONS_FILE)) {
  console.warn('[trivy-exceptions] No exceptions file found — creating empty .trivyignore');
  fs.writeFileSync(TRIVYIGNORE_FILE, '# Auto-generated by validate-trivy-exceptions.js\n');
  writeReport(0, 0, 0, []);
  process.exit(0);
}

const raw = JSON.parse(fs.readFileSync(EXCEPTIONS_FILE, 'utf8'));
const exceptions = raw.exceptions ?? [];

const today = new Date();
today.setHours(0, 0, 0, 0);
const warnThreshold = new Date(today.getTime() + WARNING_DAYS * 24 * 60 * 60 * 1000);

let validCount = 0;
let expiredCount = 0;
let invalidCount = 0;
const details = [];
const trivyIgnoreLines = [
  '# Auto-generated by validate-trivy-exceptions.js — do not edit manually.',
  `# Generated: ${new Date().toISOString()}`,
  '',
];

for (const ex of exceptions) {
  const errors = [];
  const warnings = [];

  if (!ex.id) errors.push('Missing required field: id');
  if (!ex.reason) errors.push('Missing required field: reason');
  if (!ex.owner) errors.push('Missing required field: owner');
  if (!ex.expiresAt) errors.push('Missing required field: expiresAt');

  let status = 'valid';

  if (errors.length > 0) {
    status = 'invalid';
    invalidCount++;
  } else {
    const expiry = new Date(ex.expiresAt);
    if (isNaN(expiry.getTime())) {
      errors.push(`Invalid date format for expiresAt: "${ex.expiresAt}"`);
      status = 'invalid';
      invalidCount++;
    } else if (expiry < today) {
      errors.push(`Exception expired on ${ex.expiresAt} — must be removed or renewed`);
      status = 'expired';
      expiredCount++;
    } else {
      if (expiry <= warnThreshold) {
        warnings.push(`Expires in ${Math.ceil((expiry - today) / (24 * 60 * 60 * 1000))} days (${ex.expiresAt})`);
      }
      trivyIgnoreLines.push(`# ${ex.reason} — owner: ${ex.owner} — expires: ${ex.expiresAt}`);
      trivyIgnoreLines.push(ex.id);
      trivyIgnoreLines.push('');
      validCount++;
    }
  }

  details.push({ id: ex.id ?? '(unknown)', status, warnings, errors });
}

fs.writeFileSync(TRIVYIGNORE_FILE, trivyIgnoreLines.join('\n'));
console.log(`[trivy-exceptions] .trivyignore written (${validCount} valid exceptions)`);

writeReport(validCount, expiredCount, invalidCount, details);
console.log(`[trivy-exceptions] Report written to ${REPORT_FILE}`);

if (expiredCount > 0 || invalidCount > 0) {
  console.error(`\n[trivy-exceptions] ❌ ${expiredCount} expired, ${invalidCount} invalid exceptions found.`);
  details
    .filter(d => d.status !== 'valid')
    .forEach(d => {
      console.error(`  [${d.status.toUpperCase()}] ${d.id}`);
      d.errors.forEach(e => console.error(`    ⤷ ${e}`));
    });
  process.exit(1);
}

const expiringSoon = details.filter(d => d.warnings.length > 0);
if (expiringSoon.length > 0) {
  console.warn(`\n[trivy-exceptions] ⚠️  ${expiringSoon.length} exception(s) expiring within ${WARNING_DAYS} days:`);
  expiringSoon.forEach(d => d.warnings.forEach(w => console.warn(`  ${d.id}: ${w}`)));
}

console.log(`\n[trivy-exceptions] ✅ All ${validCount} exceptions are valid.`);

function writeReport(validCount, expiredCount, invalidCount, details) {
  const report = {
    scanDate: new Date().toISOString(),
    validCount,
    expiredCount,
    invalidCount,
    details,
  };
  fs.writeFileSync(REPORT_FILE, JSON.stringify(report, null, 2));
}
